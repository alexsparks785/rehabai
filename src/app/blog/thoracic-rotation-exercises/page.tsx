      <ol>
        <li>Get into open book starting position</li>
        <li>Inhale at start position</li>
        <li>Exhale as you rotate open</li>
        <li>At end range, take 3-4 breaths, trying to rotate further with each exhale</li>
        <li>Return on inhale</li>
      </ol>

