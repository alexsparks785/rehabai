      <ul>
        <li><strong>Neck rolls:</strong> Gentle half circles, ear to shoulder—5 each direction</li>
        <li><strong>Chin tucks:</strong> Pull chin back creating a double chin—hold 5 seconds, 5 reps</li>
        <li><strong>Shoulder rolls:</strong> 10 forward, 10 backward</li>
        <li><strong>Chest opener:</strong> Clasp hands behind back, lift and open chest—hold 15 seconds</li>
      </ul>

