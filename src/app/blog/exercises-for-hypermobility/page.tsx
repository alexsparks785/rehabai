        <ol>
          <li>Lie on your back with knees bent at 90 degrees, arms reaching toward ceiling</li>
          <li>Press your lower back firmly into the floor—maintain this contact throughout</li>
          <li>Slowly lower one arm overhead while extending the opposite leg</li>
          <li>Only go as far as you can while keeping your back flat</li>
          <li>Return and switch sides</li>
          <li>10 reps per side, slow and controlled</li>
        </ol>
