      <ol>
        <li>Back flat against wall</li>
        <li>Arms in "W" position, touching wall</li>
        <li>Slide arms up to "Y" position</li>
        <li>Keep entire arm and back on wall</li>
        <li>Slide back to "W"</li>
        <li>10 slow repetitions</li>
      </ol>
