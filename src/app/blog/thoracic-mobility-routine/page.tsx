      <ol>
        <li>Hands and knees, one hand behind head</li>
        <li>Rotate to look at elbow toward floor</li>
        <li>Then rotate elbow toward ceiling</li>
        <li>Move from thoracic spine, not lumbar</li>
        <li>8-10 each side</li>
      </ol>
