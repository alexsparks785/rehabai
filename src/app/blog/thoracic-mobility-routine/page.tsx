      <ol>
        <li>Lie on side, knees stacked and bent 90°</li>
        <li>Arms extended in front, palms together</li>
        <li>Rotate top arm open, following with eyes</li>
        <li>Try to touch floor behind you</li>
        <li>Return slowly</li>
        <li>8-10 each side</li>
      </ol>

