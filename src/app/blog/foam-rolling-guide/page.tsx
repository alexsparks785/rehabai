        <ol>
          <li>Sit with the roller under your calves</li>
          <li>Cross one leg over the other for more pressure</li>
          <li>Roll from the ankle to just below the knee</li>
          <li>Rotate your leg in and out to hit inner and outer calf</li>
          <li>30-60 seconds per leg</li>
        </ol>
